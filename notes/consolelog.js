/* Para poder printar anotações na tela, usa-se o console.log */
/* Quando necessário usar aspas dentro da string é necessário alternar as aspas */
console.log('hello world!'); 
console.log("hello world!");

/* Quando eu usar as crases eu posso usar qualquer tipo de aspas,
as string dentro de crases, chamam "template string", que será visto mais pra frente no curso*/
console.log(`hello world!`);

/* Também é possível imprimir números */
console.log(2);
console.log(5.6);

